import { FigureCard } from "@/components/FigureCard";
import { MetricCard } from "@/components/MetricCard";
import { formatCurrency, formatDecimal, formatPercent } from "@/lib/format";
import {
  getFigureUrl,
  getModelSummary,
  getOverviewMetrics,
  getPrioritySummary,
  getTopRetentionTargets,
} from "@/lib/data";

export default async function HomePage() {
  const [overview, modelSummary, prioritySummary, topTargets] = await Promise.all([
    getOverviewMetrics(),
    getModelSummary(),
    getPrioritySummary(),
    getTopRetentionTargets(5),
  ]);

  const bestModel = modelSummary.bestModel;

  return (
    <div className="page-section">
      <section className="page-header">
        <div>
          <p className="eyebrow">Dashboard</p>
          <h1>Churn analytics command center</h1>
          <p>
            This page consolidates the current repository baseline: IBM Telco data, model comparison,
            explainability outputs, and retention prioritization artifacts generated by the Python pipeline.
          </p>
        </div>
        <div className="status-badge status-badge--success">Pipeline outputs loaded successfully</div>
      </section>

      <section className="grid grid--metrics">
        <MetricCard
          label="Valid customer records"
          value={overview.rowCount.toLocaleString("en-US")}
          hint="Rows retained after cleaning empty TotalCharges records."
        />
        <MetricCard
          label="Dataset columns"
          value={overview.columnCount.toLocaleString("en-US")}
          hint="Feature coverage from the current IBM Telco mainline."
        />
        <MetricCard
          label="Observed churn rate"
          value={formatPercent(overview.churnRate, 2)}
          hint="Computed from the processed dataset overview output."
        />
        <MetricCard
          label="Top-priority customers"
          value={prioritySummary.highPriorityCount.toLocaleString("en-US")}
          hint="Customers currently classified as Top Priority."
        />
      </section>

      <section className="grid grid--two">
        <article className="card">
          <p className="eyebrow">Model status</p>
          <h3>{bestModel?.model ?? "Unavailable"}</h3>
          <div className="grid" style={{ marginTop: 16 }}>
            <p className="kpi-pill">ROC-AUC {bestModel ? formatDecimal(bestModel.roc_auc) : "N/A"}</p>
            <p className="kpi-pill">F1 {bestModel ? formatDecimal(bestModel.f1_score) : "N/A"}</p>
            <p className="kpi-pill">Recall {bestModel ? formatDecimal(bestModel.recall) : "N/A"}</p>
            <p className="kpi-pill">Precision {bestModel ? formatDecimal(bestModel.precision) : "N/A"}</p>
          </div>
          <p className="muted" style={{ marginTop: 16 }}>
            Average priority score across the scored customer set: {formatDecimal(prioritySummary.averagePriorityScore, 2)}.
          </p>
        </article>

        <article className="card">
          <p className="eyebrow">Immediate retention focus</p>
          <h3>Highest-value intervention queue</h3>
          <div className="table-wrap" style={{ marginTop: 16 }}>
            <table>
              <thead>
                <tr>
                  <th>Customer ID</th>
                  <th>Churn Probability</th>
                  <th>Monthly Charges</th>
                  <th>Priority Score</th>
                </tr>
              </thead>
              <tbody>
                {topTargets.map((row) => (
                  <tr key={row.customerID}>
                    <td>{row.customerID}</td>
                    <td>{formatDecimal(row.churn_probability)}</td>
                    <td>{formatCurrency(row.MonthlyCharges)}</td>
                    <td>{formatDecimal(row.priority_score, 2)}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </article>
      </section>

      <section className="grid grid--two">
        <FigureCard
          title="Churn class distribution"
          description="Class balance view for the cleaned Telco dataset."
          src={getFigureUrl("churn_class_distribution.png")}
          alt="Churn class distribution figure"
        />
        <FigureCard
          title="ROC curve comparison"
          description="Model discrimination comparison across the evaluated classifiers."
          src={getFigureUrl("roc_curve_comparison.png")}
          alt="ROC curve comparison figure"
        />
      </section>

      <section className="grid grid--three">
        <FigureCard
          title="Contract type vs churn"
          description="Contract structure remains a core business signal for churn analysis."
          src={getFigureUrl("contract_type_vs_churn_rate.png")}
          alt="Contract type versus churn figure"
        />
        <FigureCard
          title="Monthly charges vs churn"
          description="Charge distribution differences between churned and retained customers."
          src={getFigureUrl("monthly_charges_vs_churn.png")}
          alt="Monthly charges versus churn figure"
        />
        <FigureCard
          title="Internet service vs churn"
          description="Service type segmentation with churn-rate comparison."
          src={getFigureUrl("internet_service_vs_churn.png")}
          alt="Internet service versus churn figure"
        />
      </section>
    </div>
  );
}
